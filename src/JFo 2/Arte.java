public class Arte {
    public static void main(String[] args) throws Exception {
        System.out.println("  ####  ");
        System.out.println(" #    # ");
        System.out.println("# #  # #");
        System.out.println("#      #");
        System.out.println("# #  # #");
        System.out.println("#  ##  #");
        System.out.println(" #    # ");
        System.out.println("  ####  ");
        System.out.println("\n");

        //O Java não permite colocar barra invertida no meio da string :(
        System.out.println("   ##             ##   ");
        System.out.println("  #  #___________#  #");
        System.out.println(" #                   # ");
        System.out.println("(   XX           XX   )");
        System.out.println("====       v       ====");
        System.out.println("======  (__|__)  ======");
        System.out.println("   (               )   ");
        System.out.println("    (_____________)    ");
    }
    
}
